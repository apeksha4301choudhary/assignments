package com.assignment1;
/* Write a program to convert:
○ int → double
○ double → int
*/

public class Question_3 {
	public static void main(String[] args) {
		int a = 10;
		double d = a;
		int c = (int)d;
		System.out.println("Int : " + a);
		System.out.println("Double : " + d);
		System.out.println("Int : " + c);
	}
}
